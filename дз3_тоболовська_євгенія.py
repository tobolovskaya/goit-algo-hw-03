import turtle

def koch_curve(t, order, size):
    """
    Функція для малювання кривої Коха.
    t - об'єкт turtle.
    order - рівень рекурсії.
    size - довжина лінії.
    """
    if order == 0:
        t.forward(size)
    else:
        for angle in [60, -120, 60, 0]:
            koch_curve(t, order-1, size/3)
            t.left(angle)

def koch_snowflake(t, order, size):
    """
    Функція для малювання сніжинки Коха.
    """
    for _ in range(3):
        koch_curve(t, order, size)
        t.right(120)

# Налаштування вікна turtle
window = turtle.Screen()
window.bgcolor("white")

# Створення об'єкта turtle
t = turtle.Turtle()
t.color("blue")

# Встановлення швидкості
t.speed(0)

# Визначення рівня рекурсії
recursion_level = int(input("Введіть рівень рекурсії для сніжинки Коха: "))

# Малюємо сніжинку Коха
koch_snowflake(t, recursion_level, 300)

