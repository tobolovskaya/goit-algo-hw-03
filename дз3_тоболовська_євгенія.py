import os
import shutil
from pathlib import Path

def copy_files(src, dst):
    for item in os.listdir(src):
        item_path = Path(src) / item
        if item_path.is_dir():
            # Якщо елемент є директорією, рекурсивно викликаємо функцію
            copy_files(item_path, dst)
        else:
            # Визначаємо піддиректорію на основі розширення файлу
            subdir_name = item_path.suffix[1:]  # видаляємо крапку з розширення
            if not subdir_name:
                subdir_name = 'no_extension'
            subdir_path = Path(dst) / subdir_name
            subdir_path.mkdir(parents=True, exist_ok=True)
            # Копіюємо файл
            shutil.copy(item_path, subdir_path)

